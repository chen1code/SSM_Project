package com.erp.model;

public class Specialty {
	
	private int id;
	private String specialtyName;
	private String specialtyNumber;
	private String toSchoolNumber;
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSpecialtyName() {
		return specialtyName;
	}
	public void setSpecialtyName(String specialtyName) {
		this.specialtyName = specialtyName;
	}
	public String getSpecialtyNumber() {
		return specialtyNumber;
	}
	public void setSpecialtyNumber(String specialtyNumber) {
		this.specialtyNumber = specialtyNumber;
	}
	public String getToSchoolNumber() {
		return toSchoolNumber;
	}
	public void setToSchoolNumber(String toSchoolNumber) {
		this.toSchoolNumber = toSchoolNumber;
	}
	
	

}
